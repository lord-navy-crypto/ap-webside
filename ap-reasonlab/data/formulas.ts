export function getFormulasBySubject(subject: string): Formula[] {
  return formulas.filter((f) => f.subject === subject);
}

export function getFormulaSubjects(): string[] {
  return [...new Set(formulas.map((f) => f.subject))];
}

export function getFormulaById(id: string): Formula | undefined {
  return formulas.find((f) => f.id === id);
}
